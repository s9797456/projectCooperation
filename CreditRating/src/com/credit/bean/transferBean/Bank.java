package com.credit.bean.transferBean;

import javax.xml.bind.annotation.XmlElement;

public class Bank {
	
	public Bank() {}
	
	String uuid;
	String settleAccountName;
	String settleAccountAddr;
	String settleAccountTel;
	String enterprise_infoId;
	@XmlElement
	public String getUuid() {
		return uuid;
	}
	public void setUuid(String uuid) {
		this.uuid = uuid;
	}
	@XmlElement
	public String getSettleAccountName() {
		return settleAccountName;
	}
	public void setSettleAccountName(String settleAccountName) {
		this.settleAccountName = settleAccountName;
	}
	@XmlElement
	public String getSettleAccountAddr() {
		return settleAccountAddr;
	}
	public void setSettleAccountAddr(String settleAccountAddr) {
		this.settleAccountAddr = settleAccountAddr;
	}
	@XmlElement
	public String getSettleAccountTel() {
		return settleAccountTel;
	}
	public void setSettleAccountTel(String settleAccountTel) {
		this.settleAccountTel = settleAccountTel;
	}
	@XmlElement
	public String getEnterprise_infoId() {
		return enterprise_infoId;
	}
	public void setEnterprise_infoId(String enterprise_infoId) {
		this.enterprise_infoId = enterprise_infoId;
	}
}
