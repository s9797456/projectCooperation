package com.credit.util.model;

import java.util.List;



public class Index {
	private String uuid;
	private String name;
	private String level;
	private String weight;
	private String pushed;
	private List<Option> options;
	private List<Index> chirds;
	private String insert;
	private String desc;
	public Index() {
		super();
	}
	public Index(String uuid, String name, String level, String weight,String desc,
			List<Option> options, List<Index> chirds) {
		super();
		this.uuid = uuid;
		this.name = name;
		this.level = level;
		this.weight = weight;
		this.desc = desc;
		this.options = options;
		this.chirds = chirds;
	}
	public Index(String uuid, String name, String level, String weight,String desc) {
		super();
		this.uuid = uuid;
		this.name = name;
		this.level = level;
		this.weight = weight;
		this.desc = desc;
	}
	public Index(String uuid, String name, String level, String weight,String pushed,String desc,
			List<Option> options,String insert) {
		super();
		this.uuid = uuid;
		this.name = name;
		this.level = level;
		this.weight = weight;
		this.pushed = pushed;
		this.desc = desc;
		this.options = options;
		this.insert=insert;
	}
	/**
	 * @return the uuid
	 */
	public String getUuid() {
		return uuid;
	}
	/**
	 * @param uuid the uuid to set
	 */
	public void setUuid(String uuid) {
		this.uuid = uuid;
	}
	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}
	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}
	/**
	 * @return the level
	 */
	public String getLevel() {
		return level;
	}
	/**
	 * @param level the level to set
	 */
	public void setLevel(String level) {
		this.level = level;
	}
	/**
	 * @return the weight
	 */
	public String getWeight() {
		return weight;
	}
	/**
	 * @param weight the weight to set
	 */
	public void setWeight(String weight) {
		this.weight = weight;
	}
	/**
	 * @return the options
	 */
	public List<Option> getOptions() {
		return options;
	}
	/**
	 * @param options the options to set
	 */
	public void setOptions(List<Option> options) {
		this.options = options;
	}
	/**
	 * @return the chirds
	 */
	public List<Index> getChirds() {
		return chirds;
	}
	/**
	 * @param chirds the chirds to set
	 */
	public void setChirds(List<Index> chirds) {
		this.chirds = chirds;
	}
	
	/* (non-Javadoc)
	 * @see java.lang.Object#toString()
	 */
	/* (non-Javadoc)
	 * @see java.lang.Object#toString()
	 */
	@Override
	public String toString() {
		return "Index [uuid=" + uuid + ", name=" + name + ", level=" + level
				+ ", weight=" + weight + ", options=" + options.toString() + ", chirds="
				+ chirds + "]";
	}
	/**
	 * @return the insert
	 */
	public String getInsert() {
		return insert;
	}
	/**
	 * @param insert the insert to set
	 */
	public void setInsert(String insert) {
		this.insert = insert;
	}
	/**
	 * @return the desc
	 */
	public String getDesc() {
		return desc;
	}
	/**
	 * @param desc the desc to set
	 */
	public void setDesc(String desc) {
		this.desc = desc;
	}
	/**
	 * @return the pushed
	 */
	public String getPushed() {
		return pushed;
	}
	/**
	 * @param pushed the pushed to set
	 */
	public void setPushed(String pushed) {
		this.pushed = pushed;
	}
	

	
}
