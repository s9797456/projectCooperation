package com.credit.util.model;

public class IndexVO {
	private String uuid;
	private String indexName;
	private double indexScore;
	private String weight;
	private String optionName;
	private double optionScore;
	/**
	 * @return the uuid
	 */
	public String getUuid() {
		return uuid;
	}
	/**
	 * @param uuid the uuid to set
	 */
	public void setUuid(String uuid) {
		this.uuid = uuid;
	}
	/**
	 * @return the weight
	 */
	public String getWeight() {
		return weight;
	}
	/**
	 * @param weight the weight to set
	 */
	public void setWeight(String weight) {
		this.weight = weight;
	}
	/**
	 * @return the optionName
	 */
	public String getOptionName() {
		return optionName;
	}
	/**
	 * @param optionName the optionName to set
	 */
	public void setOptionName(String optionName) {
		this.optionName = optionName;
	}
	/**
	 * @return the optionScore
	 */
	public double getOptionScore() {
		return optionScore;
	}
	/**
	 * @param optionScore the optionScore to set
	 */
	public void setOptionScore(double optionScore) {
		this.optionScore = optionScore;
	}
	/**
	 * @return the indexName
	 */
	public String getIndexName() {
		return indexName;
	}
	/**
	 * @param indexName the indexName to set
	 */
	public void setIndexName(String indexName) {
		this.indexName = indexName;
	}
	/**
	 * @return the indexScore
	 */
	public double getIndexScore() {
		return indexScore;
	}
	/**
	 * @param indexScore the indexScore to set
	 */
	public void setIndexScore(double indexScore) {
		this.indexScore = indexScore;
	}
	/* (non-Javadoc)
	 * @see java.lang.Object#toString()
	 */
	@Override
	public String toString() {
		return "IndexVO [uuid=" + uuid + ", indexName=" + indexName
				+ ", indexScore=" + indexScore + ", weight=" + weight
				+ ", optionName=" + optionName + ", optionScore=" + optionScore
				+ "]";
	}
	
	

}
